package java_Homework;

public class Maths {

	public static void main(String[] args) {
		 
		System.out.println(Math.abs(-2.278)); 
		System.out.println(Math.abs(+2.278));
		System.out.println(Math.ceil(-7.6)); //it give round figure number as 7
		System.out.println(Math.floor(3.9));// it gives rounde figure number as 4
		System.out.println(Math.max(3, 7)); //prints maximum number
		System.out.println(Math.min(2.4, 7.9));//prints minmum number
		System.out.println(Math.getExponent(4)); // prints exponent of 4
		System.out.println(Math.pow(5, 2)); //gives output of 5^2
		System.out.println(Math.sqrt(10)); // prints squareroot of 10 output

	}

}
